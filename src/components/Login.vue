<template>
    <div class="login_container">
        <div class="login_box">
            <!-- 头像区域 -->
            <div class="avatar_box">
                <img src="../assets/logo.png" alt="">
            </div>
            <!-- 登录表单区域 -->
            <el-form ref="loginFormRef" :model="loginForm" :rules="loginFormRules" label-width="0px" class="login_form">
                <!-- 用户名 -->
                <el-form-item prop="username">
                    <el-input v-model="loginForm.username" prefix-icon="iconfont icon-user"></el-input>
                </el-form-item>
                <!-- 密码 -->
                <el-form-item prop="password">
                    <el-input v-model="loginForm.password" type="password" prefix-icon="iconfont icon-3702mima">
                    </el-input>
                </el-form-item>
                <!-- 按钮区域 -->
                <el-form-item class="btns">
                    <el-button type="primary" @click="login">登录</el-button>
                    <el-button type="info" @click="resetLoginForm">重置</el-button>
                </el-form-item>
            </el-form>
        </div>
    </div>
</template>

<script>
    export default {
        data() {
            return {
                //这是登陆表单loginForm的登陆绑定对象
                loginForm: {
                    username: 'admin',
                    password: '123456'
                },
                //这是表单的验证规则对象
                loginFormRules: {
                    //验证用户名是否合法
                    username: [{
                            required: true,
                            message: '请输入登录名称',
                            trigger: 'blur'
                        },
                        {
                            min: 3,
                            max: 10,
                            message: '长度在 3 到 10 个字符',
                            trigger: 'blur'
                        }
                    ],
                    //验证密码是否合法
                    password: [{
                            required: true,
                            message: '请输入登录密码',
                            trigger: 'blur'
                        },
                        {
                            min: 3,
                            max: 10,
                            message: '长度在 3 到 10 个字符',
                            trigger: 'blur'
                        }
                    ]
                }
            }
        },
        methods: {
            //点击登录按钮，重置登录表单
            resetLoginForm() {
                this.$refs.loginFormRef.resetFields();
            },
            login() {
                this.$refs.loginFormRef.validate(async valid => {
                    if (!valid) {
                        return;
                    } else {
                        //通过this访问原型上的$http成员通过它发送post请求   路径是login   将loginForm表单传给页面 
                        this.$http.post('login', this.loginForm)
                            .then(res => {
                                console.log(res)
                                // element ui 组件中有message弹窗组件，其中有一系列方法可以弹出提示消息
                                if (res.data.meta.status !== 200) return this.$message.error("登录失败！");
                                this.$message.success("登陆成功！")
                                // 1.将登陆成功之后的token，保存到客户端的sessionStorage(会话期间的存储机制)中
                                // 1.1 项目中除了登录之外的其它api接口，必须在登陆之后才能访问
                                // 1.2 token只应在当前网站打开期间生效，所以token保存在sessionStorage中
                                window.sessionStorage.setItem("token",res.data.data.token);
                                // 2. 通过编程式导航跳转到后台主页，路由地址是/home
                                this.$router.push("/home");
                            })
                    }
                })
            },
        }


    }
    // lang="less"说明该节点中支持less语法格式
    //scoped 是vue的指令用来控制组建的样式生效区间  加上他就在当前组件内生效  去掉就全局生效
</script>

